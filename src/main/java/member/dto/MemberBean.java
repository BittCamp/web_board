package member.dto;

public class MemberBean {

	private String id;
	private String name;
	//setter
	public void setId(String id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
	//getter
	public String getId() {
		return id;
	}
	public String getName() {
		return name;
	}
}
